/**
 * Reads `terraform output -json` from infra/aws_tf and generates:
 * - CloudWatch / OTel defaults (TS + Python + JSON summary)
 * - Selected `app_config.toml` files (iam, notification, storage bucket, general AI Bedrock,
 *   arch diagram agent Bedrock + DynamoDB)
 *
 * Usage (repo root): node infra/aws_tf/scripts/sync-cloudwatch-logging-defaults.mjs
 * Or: npm run sync:cloudwatch-logging | npm run sync:terraform-outputs (from frontend/ or aspire.svc/).
 */
import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const TF_DIR = join(__dirname, "..");

const STREAM_PREFIX = {
  frontend: "arb-frontend",
  aspire: "arb-aspire",
  iam_svc: "iam-svc",
  general_ai_agent_svc: "general-ai-agent-svc",
  arch_diagram_agent_svc: "arch-diagram-agent-svc",
  document_storage_svc: "document-storage-svc",
  solutions_svc: "solutions-svc",
  storage_svc: "storage-svc",
};

function tfOutputJson() {
  return JSON.parse(
    execFileSync("terraform", ["output", "-json"], {
      cwd: TF_DIR,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    }),
  );
}

function emitTsDefaults({ workloadKey, outPath, outputs }) {
  const names = outputs.cloudwatch_application_log_group_names.value;
  const identity = outputs.application_logging_service_identity.value;
  const policyArn = outputs.application_cloudwatch_logs_put_policy_arn.value;
  const region = outputs.aws_region.value;
  const accountId = outputs.aws_account_id.value;
  const id = identity[workloadKey];
  const logGroup = names[workloadKey];
  const body = `/* eslint-disable -- generated by infra/aws_tf/scripts/sync-cloudwatch-logging-defaults.mjs */
export const CLOUDWATCH_TERRAFORM_DEFAULTS = {
  workloadKey: ${JSON.stringify(workloadKey)},
  awsRegion: ${JSON.stringify(region)},
  awsAccountId: ${JSON.stringify(accountId)},
  applicationLogsPutPolicyArn: ${JSON.stringify(policyArn)},
  logGroupName: ${JSON.stringify(logGroup)},
  otelServiceName: ${JSON.stringify(id.service_name)},
  serviceId: ${JSON.stringify(id.service_id)},
  logStreamPrefix: ${JSON.stringify(STREAM_PREFIX[workloadKey] ?? workloadKey)},
} as const;
`;
  writeFileSync(outPath, body, "utf8");
}

function emitPyDefaults({ workloadKey, outPath, outputs }) {
  const names = outputs.cloudwatch_application_log_group_names.value;
  const identity = outputs.application_logging_service_identity.value;
  const policyArn = outputs.application_cloudwatch_logs_put_policy_arn.value;
  const region = outputs.aws_region.value;
  const accountId = outputs.aws_account_id.value;
  const id = identity[workloadKey];
  const logGroup = names[workloadKey];
  const prefix = STREAM_PREFIX[workloadKey] ?? workloadKey;
  const body = `# Generated by infra/aws_tf/scripts/sync-cloudwatch-logging-defaults.mjs — do not edit by hand.
"""Terraform-synced defaults for CloudWatch logging (OpenTelemetry → PutLogEvents)."""

DEFAULT_AWS_REGION = ${JSON.stringify(region)}
DEFAULT_AWS_ACCOUNT_ID = ${JSON.stringify(accountId)}
DEFAULT_LOG_GROUP = ${JSON.stringify(logGroup)}
LOG_STREAM_PREFIX = ${JSON.stringify(prefix)}
DEFAULT_OTEL_SERVICE_NAME = ${JSON.stringify(id.service_name)}
DEFAULT_SERVICE_ID = ${JSON.stringify(id.service_id)}
APPLICATION_LOGS_PUT_POLICY_ARN = ${JSON.stringify(policyArn)}
WORKLOAD_KEY = ${JSON.stringify(workloadKey)}
`;
  writeFileSync(outPath, body, "utf8");
}

/**
 * @param {Record<string, { value: unknown }>} out
 * @param {string} repoRoot
 */
function syncAppConfigTomlFromOutputs(out, repoRoot) {
  const region = String(out.aws_region.value);
  const iamPath = join(repoRoot, "iam.svc/server/app_config.toml");
  const iamBody = `# iam.svc — gRPC + DynamoDB. Table names mirror Terraform outputs (infra/aws_tf).
# Regenerate: node infra/aws_tf/scripts/sync-cloudwatch-logging-defaults.mjs
# Override path with IAM_APP_CONFIG_PATH.

[app]
service_name = "iam-service"
log_level = "DEBUG"
version = "0.1.0"

[api_service]
host = "0.0.0.0"
port = 8803

[dynamodb]
region = ${JSON.stringify(region)}
endpoint_url = ""

[dynamodb.tables]
users = ${JSON.stringify(String(out.ddb_iam_users_table_name.value))}
user_types = ${JSON.stringify(String(out.ddb_iam_user_types_table_name.value))}
login_types = ${JSON.stringify(String(out.ddb_iam_login_types_table_name.value))}
logins = ${JSON.stringify(String(out.ddb_iam_logins_table_name.value))}
skill_lists = ${JSON.stringify(String(out.ddb_iam_skill_lists_table_name.value))}
skills = ${JSON.stringify(String(out.ddb_iam_skills_table_name.value))}
user_skills = ${JSON.stringify(String(out.ddb_iam_user_skills_table_name.value))}
sessions = ${JSON.stringify(String(out.ddb_iam_sessions_table_name.value))}
invites = ${JSON.stringify(String(out.ddb_iam_invites_table_name.value))}
deployment_admin = ${JSON.stringify(String(out.ddb_iam_deployment_admin_table_name.value))}
`;
  writeFileSync(iamPath, iamBody, "utf8");

  const notifPath = join(repoRoot, "notification.svc/server/app_config.toml");
  const snsRegion = String(out.notification_sns_region.value);
  const topicArn = String(out.notification_sns_topic_arn.value);
  const notifBody = `# notification.svc — gRPC + Amazon SNS (email via topic subscriptions).
# Regenerate: node infra/aws_tf/scripts/sync-cloudwatch-logging-defaults.mjs
# Override path with NOTIFICATION_SVC_APP_CONFIG_PATH.

[app]
service_name = "notification-service"
log_level = "DEBUG"
version = "0.1.0"

[api_service]
host = "0.0.0.0"
port = 8807

[sns]
region = ${JSON.stringify(snsRegion)}
endpoint_url = ""
topic_arn = ${JSON.stringify(topicArn)}
`;
  writeFileSync(notifPath, notifBody, "utf8");

  const storagePath = join(repoRoot, "storage.svc/server/app_config.toml");
  let storageToml = readFileSync(storagePath, "utf8");
  const bucket = String(out.s3_exlservice_arb_general_bucket_name.value);
  if (!/^bucket = ".*"$/m.test(storageToml)) {
    throw new Error("storage app_config.toml: expected a bucket = line to replace");
  }
  storageToml = storageToml.replace(/^bucket = ".*"$/m, `bucket = ${JSON.stringify(bucket)}`);
  writeFileSync(storagePath, storageToml, "utf8");

  const agentPath = join(repoRoot, "general.ai.agent.svc/server/app_config.toml");
  let agentToml = readFileSync(agentPath, "utf8");
  const foundation = String(out.general_ai_agent_bedrock_foundation_model_id.value);
  const inference = String(out.general_ai_agent_bedrock_inference_profile_id.value);
  agentToml = agentToml.replace(
    /^region = ".*"$/m,
    `region = ${JSON.stringify(region)}`,
  );
  agentToml = agentToml.replace(
    /^foundation_model_id = ".*"$/m,
    `foundation_model_id = ${JSON.stringify(foundation)}`,
  );
  agentToml = agentToml.replace(
    /^inference_profile_id = ".*"$/m,
    `inference_profile_id = ${JSON.stringify(inference)}`,
  );
  agentToml = agentToml.replace(
    /^strands_model_id = ".*"$/m,
    `strands_model_id = ${JSON.stringify(inference)}`,
  );
  writeFileSync(agentPath, agentToml, "utf8");

  const archAgentPath = join(repoRoot, "arch.diagram.agent.svc/server/app_config.toml");
  let archAgentToml = readFileSync(archAgentPath, "utf8");
  const archFoundation = String(out.arch_diagram_agent_bedrock_foundation_model_id.value);
  const archInference = String(out.arch_diagram_agent_bedrock_inference_profile_id.value);
  const conversionJobsTable = String(out.ddb_arch_diagram_conversion_jobs_table_name.value);
  archAgentToml = archAgentToml.replace(
    /^region = ".*"$/gm,
    `region = ${JSON.stringify(region)}`,
  );
  archAgentToml = archAgentToml.replace(
    /^foundation_model_id = ".*"$/m,
    `foundation_model_id = ${JSON.stringify(archFoundation)}`,
  );
  archAgentToml = archAgentToml.replace(
    /^inference_profile_id = ".*"$/m,
    `inference_profile_id = ${JSON.stringify(archInference)}`,
  );
  archAgentToml = archAgentToml.replace(
    /^strands_model_id = ".*"$/m,
    `strands_model_id = ${JSON.stringify(archInference)}`,
  );
  if (!/^conversion_jobs = ".*"$/m.test(archAgentToml)) {
    throw new Error("arch diagram app_config.toml: expected conversion_jobs = line to replace");
  }
  archAgentToml = archAgentToml.replace(
    /^conversion_jobs = ".*"$/m,
    `conversion_jobs = ${JSON.stringify(conversionJobsTable)}`,
  );
  writeFileSync(archAgentPath, archAgentToml, "utf8");

  const docstorePath = join(repoRoot, "document-storage.svc/server/app_config.toml");
  let docstoreToml = readFileSync(docstorePath, "utf8");
  const registryTable = String(out.ddb_docstore_registry_table_name.value);
  const groupsTable = String(out.ddb_docstore_groups_table_name.value);
  const attachmentsBucket = String(out.docstore_attachments_bucket_name.value);
  const opensearchEndpoint = String(out.docstore_opensearch_collection_endpoint.value);
  docstoreToml = docstoreToml.replace(
    /^docstore_registry = ".*"$/m,
    `docstore_registry = ${JSON.stringify(registryTable)}`,
  );
  docstoreToml = docstoreToml.replace(
    /^docstore_groups = ".*"$/m,
    `docstore_groups = ${JSON.stringify(groupsTable)}`,
  );
  docstoreToml = docstoreToml.replace(/^bucket = ".*"$/m, `bucket = ${JSON.stringify(attachmentsBucket)}`);
  docstoreToml = docstoreToml.replace(
    /^collection_endpoint = ".*"$/m,
    `collection_endpoint = ${JSON.stringify(opensearchEndpoint)}`,
  );
  writeFileSync(docstorePath, docstoreToml, "utf8");
}

function main() {
  const out = tfOutputJson();
  mkdirSync(join(TF_DIR, "generated"), { recursive: true });

  const repoRoot = join(TF_DIR, "..", "..");
  syncAppConfigTomlFromOutputs(out, repoRoot);

  emitTsDefaults({
    workloadKey: "frontend",
    outPath: join(repoRoot, "frontend/lib/config/cloudwatch-logging.defaults.generated.ts"),
    outputs: out,
  });
  emitTsDefaults({
    workloadKey: "aspire",
    outPath: join(repoRoot, "aspire.svc/lib/config/cloudwatch-logging.defaults.generated.ts"),
    outputs: out,
  });

  const pyTargets = [
    {
      workloadKey: "iam_svc",
      outPath: join(
        repoRoot,
        "iam.svc/server/src/iam_service/core/cloudwatch_logging_defaults_generated.py",
      ),
    },
    {
      workloadKey: "general_ai_agent_svc",
      outPath: join(
        repoRoot,
        "general.ai.agent.svc/server/src/general_ai_agent_service/core/cloudwatch_logging_defaults_generated.py",
      ),
    },
    {
      workloadKey: "arch_diagram_agent_svc",
      outPath: join(
        repoRoot,
        "arch.diagram.agent.svc/server/src/arch_diagram_agent_service/core/cloudwatch_logging_defaults_generated.py",
      ),
    },
    {
      workloadKey: "document_storage_svc",
      outPath: join(
        repoRoot,
        "document-storage.svc/server/src/document_storage_service/core/cloudwatch_logging_defaults_generated.py",
      ),
    },
    {
      workloadKey: "solutions_svc",
      outPath: join(
        repoRoot,
        "solutions.svc/server/src/solutions_service/core/cloudwatch_logging_defaults_generated.py",
      ),
    },
    {
      workloadKey: "storage_svc",
      outPath: join(
        repoRoot,
        "storage.svc/server/src/storage_service/core/cloudwatch_logging_defaults_generated.py",
      ),
    },
  ];

  for (const t of pyTargets) {
    emitPyDefaults({ workloadKey: t.workloadKey, outPath: t.outPath, outputs: out });
  }

  const jsonPath = join(TF_DIR, "generated/cloudwatch-logging.defaults.json");
  writeFileSync(
    jsonPath,
    JSON.stringify(
      {
        syncedFrom: "terraform output",
        aws_region: out.aws_region.value,
        aws_account_id: out.aws_account_id.value,
        application_logs_put_policy_arn:
          out.application_cloudwatch_logs_put_policy_arn.value,
        log_group_names: out.cloudwatch_application_log_group_names.value,
        service_identity: out.application_logging_service_identity.value,
      },
      null,
      2,
    ),
    "utf8",
  );
}

main();
